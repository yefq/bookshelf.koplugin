-- KOReader's localised name lookup. Optional: if the module is unavailable
-- (odd build/test context) we degrade to using the code itself as the label.
local _iso = nil
do
    local ok, mod = pcall(require, "ui/data/isolanguage")
    if ok then _iso = mod end
end

local function _localizedName(iso3)
    if _iso and _iso.getLocalizedLanguage then
        local ok, name = pcall(_iso.getLocalizedLanguage, _iso, iso3)
        if ok and name then return name end
    end
    return iso3
end

-- name (lowercased) -> iso3, built from the codes we bridge. Lets a book that
-- declares "English" land on the same key as one declaring "en". Localised to
-- the current UI language, since the names come from KOReader.
local _NAME_TO_ALPHA3
local function _nameToAlpha3()
    if _NAME_TO_ALPHA3 then return _NAME_TO_ALPHA3 end
    local map = {}
    for _alpha2, iso3 in pairs(ALPHA2_TO_ALPHA3) do
        local name = _localizedName(iso3)
        if type(name) == "string" and name ~= iso3 then
            map[name:lower()] = iso3
        end
    end
    _NAME_TO_ALPHA3 = map
    return map
end

local function _trim(s) return (s:gsub("^%s+", ""):gsub("%s+$", "")) end

-- canonical(raw): returns (key, label) for a language value, or nil for an
-- empty/absent value (the caller owns the "Unknown" bucket).
--   key   - stable grouping key; every spelling of one language shares it
--   label - friendly, localised display name (falls back to the raw value)
function M.canonical(raw)
    if type(raw) ~= "string" then return nil end
    local trimmed = _trim(raw)
    if trimmed == "" then return nil end
    local lower = trimmed:lower()

    -- Full-name match first ("english", "modern greek") before we strip
    -- anything region-like off a code.
    local by_name = _nameToAlpha3()[lower]
    if by_name then return by_name, _localizedName(by_name) end

    -- Primary subtag: drop region/script ("en-US" -> "en", "zh_Hans" -> "zh").
    local primary = lower:match("^([^-_]+)") or lower
    if #primary == 2 and ALPHA2_TO_ALPHA3[primary] then
        local iso3 = ALPHA2_TO_ALPHA3[primary]
        return iso3, _localizedName(iso3)
    end
    if #primary == 3 and primary:match("^%a%a%a$") then
        return primary, _localizedName(primary)
    end

    -- Unresolved: keep a normalised key (so case/region variants still merge)
    -- and show the book's own value as the best-effort label.
    return primary, trimmed
end

return M
