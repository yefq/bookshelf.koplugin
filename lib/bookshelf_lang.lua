-- bookshelf_lang.lua
-- Canonicalise ebook language metadata for the Languages grouping/filter.
--
-- Ebook <dc:language> values are a mess across a real library: 2-letter
-- ISO 639-1 codes ("en"), 3-letter ISO 639-3 codes ("eng"), region-tagged
-- BCP47 tags ("en-US", "zh-Hans"), and occasionally full names ("English").
-- Grouping on the raw value splits one language across several cards and
-- labels them with cryptic codes. canonical() folds every spelling of a
-- language onto a single key and hands back a friendly, localised label.
--
-- Names come from KOReader's ui/data/isolanguage (localised, maintained).
-- That module keeps its code tables private and only exposes a 3-letter
-- lookup, so we bundle the small, frozen ISO 639-1 -> 639-3 bridge here and
-- derive the name<->code reverse lookup from KOReader at load.

local M = {}

-- ISO 639-1 (alpha-2) -> ISO 639-3 (alpha-3). Generated by inverting
-- KOReader's iso_bcp47_map, so every value resolves to a name via
-- getLocalizedLanguage. Reference data: stable, not expected to change.
local ALPHA2_TO_ALPHA3 = {
